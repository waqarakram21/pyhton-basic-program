set_a = {1,2,5,3,4,4,5}
set_b = {4,5,6,7,8,9}

print(set_a | set_b)#   this is the union of two sets.
print(set_a & set_b)#   this is the intersection of two sets.
print(set_a - set_b)#   this is the difference fo two sets.
print(set_a ^ set_b)#   this is the symmetric intersection of two sets.
#   these two sets are set_a and set_b.


